package com.lazite.assist.produce.jpa.pojo.ftl.pages.jsp.v1.jsp;

public class Jsp {
	
	private String projectName;//项目名
	
	private String projectNameCN;//项目中文名
	
	private String moduleName;//模块名
	
	private String moduleNameCN;//中文模块名称
	
	private String extend;//对name的扩展

	public String getProjectName() {
		return projectName;
	}

	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}

	public String getProjectNameCN() {
		return projectNameCN;
	}

	public void setProjectNameCN(String projectNameCN) {
		this.projectNameCN = projectNameCN;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getModuleNameCN() {
		return moduleNameCN;
	}

	public void setModuleNameCN(String moduleNameCN) {
		this.moduleNameCN = moduleNameCN;
	}

	public String getExtend() {
		return extend;
	}

	public void setExtend(String extend) {
		this.extend = extend;
	}
	
	
}
